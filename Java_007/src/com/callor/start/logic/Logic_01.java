package com.callor.start.logic;

/*
 * 논리(Logic) 연산
 * 참(yes, true), 거짓(no, false)를 판단하여
 * 어떤 행위를 수행하는 연산
 */
public class Logic_01 {

	public static void main(String args[]) {
		
		// 3이 3 미만이냐(보다작냐)
		System.out.println( 3 < 3 );
		System.out.println( 3 > 3 );
		
		// 3이 3과 같냐
		System.out.println( 3 == 3 );
		
		// 3이 3(이하)보다 작거나 같냐
		System.out.println( 3 <= 3 );
		
		// 3이 3(이상)보다 크거나 같냐
		System.out.println( 3 >= 3 );
		
		
		
	}
}
